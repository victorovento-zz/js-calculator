const erase = () => {

    if (document.getElementById("pantalla").innerText.length <= 12 && document.getElementById("pantalla").innerText.length > 1) {

        let temp = document.getElementById("pantalla").innerText
        let actual = "";

        for (let index = 0; index < temp.length - 1; index++) {
            actual = actual + temp.charAt(index)
        }

        document.getElementById("pantalla").innerText = actual;
    } else if (document.getElementById("pantalla").innerText.length == 1) {
        clearScreen();
    }
}

const clearScreen = () => {
    document.getElementById("pantalla").innerText = "0";
}

const showResult = () => {
    try {
        let expression = document.getElementById("pantalla").innerText;
        document.getElementById("pantalla").innerText = eval(expression);
    } catch (error) {
        document.getElementById("pantalla").innerText = "SINTAX ERROR";
    }
}


